 package com.hxqh.report;
 
 public class MXReportDataFormatException extends Exception
 {
   public MXReportDataFormatException()
   {
   }
 
   public MXReportDataFormatException(String message, Throwable cause)
   {
    super(message, cause);
   }
 
   public MXReportDataFormatException(String message)
   {
     super(message);
   }
 
   public MXReportDataFormatException(Throwable cause)
   {
     super(cause);
   }
 }
