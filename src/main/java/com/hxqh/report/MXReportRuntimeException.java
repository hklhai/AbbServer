package com.hxqh.report;

public class MXReportRuntimeException extends RuntimeException
{
	public MXReportRuntimeException()
	{
	}

	public MXReportRuntimeException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public MXReportRuntimeException(String message)
	{
		super(message);
	}

	public MXReportRuntimeException(Throwable cause)
	{
		super(cause);
	}
}

